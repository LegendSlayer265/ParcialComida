    public void refreshTable(){
        DefaultTableModel dtm = (DefaultTableModel) TablaComida.getModel();
        dtm.setRowCount(0);
        ArrayList<Comida> list = DaoComida.getAllRecords();
        Iterator<Comida> itr = list.iterator();
        while(itr.hasNext()){
            Comida comidaObj = itr.next();
            dtm.addRow(new Object[]{comidaObj.getId(),comidaObj.getComida()});
        }  
    }
    
