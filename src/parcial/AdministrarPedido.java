    private void cargarComida(){
        DefaultComboBoxModel<String> model = new DefaultComboBoxModel<>();
        ArrayList<Comida> comida = DaoComida.getAllRecords();
        Iterator<Comida> itr = comida.iterator();
        while (itr.hasNext()) {
            Comida comidas = itr.next();
            model.addElement(comidas.getComida());
        }
        jComboBoxComida.setModel(model);   
    }
    
